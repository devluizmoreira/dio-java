public class modificadoresDeClasses {
    
    //abstract
    // - Classe que não pode ser instanciada ou método que precisa ser implementado por uma subclasse não abstrata
    
    //class
    // - Especifica uma classe

    //extends
    // - Indica a superclasse que a subclasse está estendendo

    //final
    // - Impossibilita que uma classe seja estendida, que um método seja sobrescrito ou que uma variável seja reinicializada

    //implementes
    // - Indica as interfaces que uma classe irá implementar

    //interface
    // - Especifica uma interface

    //native
    // - Indica que um método está escrito em uma linguagem dependente de plataforma, como o C

    //new
    // - Instancia um novo objeto, chamando seu contrutor

    //static
    // -  Faz um método ou variável pertencer à classe ao invés de às instâncias

    //strictfp
    // - Usado em frente a um método ou classe para indicar que os números de ponto flutuante seguirão as regras de ponto flutuante em todas as expressões

    //synchronized
    // - Indica que um método só pode ser acessado por uma thread de cada vez

    //transient
    // - Impede a serialização de campos

    //volatile
    // - Indica que uma variável pode ser alterada durante o uso de threads



}
